function background() {
    const el = document.querySelector(".background.color");
    el.classList.add("background-grey");  //background() background-grey        
}

function textcolor() {
    const text = document.querySelector(".text.color");
    text.classList.add("color-aqua");
}

function border() {
    const border = document.querySelector(".border.radius") //html
    border.classList.add("border-radius") // css
}